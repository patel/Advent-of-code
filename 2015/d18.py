from collections import defaultdict

def generate_grid_from_input(input_str):
    return map(lambda x: map(lambda t: 1 if t == '#' else 0, list(x)), input_str.split('\n'))


def get_next_state(grid):
    new_grid = [x[:] for x in [[0]*len(grid)]*len(grid)]
    for row in range(len(grid)):
        for column in range(len(grid[row])):
            short_circuit = False
            starting_row = (row or 1) - 1
            ending_row = row + 1 if row < (len(grid)  -1) else len(grid) -1
            starting_col = (column or 1) - 1
            ending_col = column + 1 if column < (len(grid[row]) -1)  else len(grid[row]) -1
            switched_neighbors = 0
            for i in range(starting_row, ending_row + 1):
                switched_neighbors = switched_neighbors + sum(grid[i][starting_col:ending_col+1])
                if i == row:
                    switched_neighbors = switched_neighbors - grid[row][column]
                if switched_neighbors > 3:
                    new_grid[row][column] = 0
                    short_circuit = True
                    break
            if short_circuit:
                continue

            if grid[row][column] == 0 and switched_neighbors == 3:
                new_grid[row][column] = 1
            if grid[row][column] == 1 and switched_neighbors > 1:
                new_grid[row][column] = 1
    return new_grid


def get_num_of_on_lights(input_str, num_steps):
    y = generate_grid_from_input(input_str)
    i = 0
    while i < num_steps:
        y = get_next_state(y)
        i = i + 1
    return sum(map(lambda x: sum(x), y))


def apply_defect(grid):
    size = len(grid)
    for i in [0, size - 1]:
        for j in [0, size - 1]:
            grid[i][j] = 1


def get_num_of_on_lights_for_defective(input_str, num_steps):
    y = generate_grid_from_input(input_str)
    apply_defect(y)
    i = 0
    while i < num_steps:
        y = get_next_state(y)
        apply_defect(y)
        i = i + 1
    return sum(map(lambda x: sum(x), y))


